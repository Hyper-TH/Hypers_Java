package arrays_strings.src.Lesson18;

public class Lesson18
{
    public static void main(String[] args)
    {
        String str = "I love cheese";

        System.out.println(str);

        str = str.replace("cheese", "fruits");

        System.out.println(str);
    } // end main()

} // end Lesson18 class
