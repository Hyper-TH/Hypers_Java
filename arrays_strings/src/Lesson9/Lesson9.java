package arrays_strings.src.Lesson9;

public class Lesson9
{
    public static void main(String[] args)
    {
        String car1 = "Ford";
        String car2 = "Chevy";
        String car3 = "Mitsubishi";
        String str = "I love cars, they're a great deal.";

        System.out.println(car1);
        System.out.println(car1.length() + "\n");

        System.out.println(car2);
        System.out.println(car2.length() + "\n");

        System.out.println(car3);
        System.out.println(car3.length() + "\n");

        System.out.println(str);
        System.out.println(str.length() + "\n");

    } // end main()

} // end Lesson9 class